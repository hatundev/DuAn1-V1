    private VouCherResponse getVouCher() {
        VouCherResponse vc = new VouCherResponse();
        if (index >= 0 && index < list.size()) {
            vc.setId(list.get(index).getId());
            vc.setSTT(list.get(index).getSTT());
        }

        try {
            vc.setMaVouCher(txtMagiamGia.getText().trim());
            vc.setTenVouCher(txtTenPhieuGiamGia.getText().trim());
            vc.setGiaTriToiThieuApDung(Float.parseFloat(txtGiaTriToiThieuDuocApDung.getText().trim()));
            vc.setPhanTramGiam(Integer.parseInt(txtPhanTramGiam.getText().trim()));
            if (vc.getPhanTramGiam() >= 100) {
                JOptionPane.showMessageDialog(this, "Phần trăm giảm phải từ 0-100", "Lỗi", 0);
                return null;
            }
            vc.setSoTienDuocGiamToiDa(Float.parseFloat(txtSoTienToiDaDuocGiam.getText().trim()));
            vc.setNgayBatDau(txtNgayBatDau.getText().trim());
            vc.setNgayKetThuc(txtNgayKetThuc.getText().trim());
            LocalDate startDate = LocalDate.parse(vc.getNgayBatDau());
            LocalDate endDate = LocalDate.parse(vc.getNgayKetThuc());
            LocalDate today = LocalDate.now();
            if (startDate.isBefore(today)) {
                JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải sau hôm nay", "Lỗi", 0);
                return null;
            }
            
            if (startDate.isBefore(endDate)) {
                
            } else if (startDate.isEqual(endDate)) {
                JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải khác ngày kết thúc", "Lỗi", 0);
                return null;
            } else {
                JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải trước ngày kết thúc", "Lỗi", 0);
                return null;
            }
            
            vc.setNguoiTao("admin");
            vc.setSoLuong(Integer.parseInt(txtSoLuong.getText().trim()));
            vc.setHoatDong(1);
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập các giá trị số hợp lệ!");
            return null;
        }

        return vc;
    }

