    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        VouCherResponse vc = getVouCher();

        if (vc == null) {

            return;
        }

        // Kiểm tra thông tin nhập vào
        if (vc.getMaVouCher().isEmpty()
                || vc.getTenVouCher().isEmpty()
                || vc.getGiaTriToiThieuApDung() <= 0
                || vc.getPhanTramGiam() <= 0
                || vc.getSoTienDuocGiamToiDa() <= 0
                || vc.getNgayBatDau().isEmpty()
                || vc.getNgayKetThuc().isEmpty()
                || vc.getSoLuong() <= 0) {

            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin hợp lệ!");
            return;
        }

        // Thực hiện thêm voucher vào cơ sở dữ liệu
        boolean success = vouCherRepository.getADD(vc);

        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm voucher thành công!");
            list.add(vc);
            Clear();

            FormVoucher formVouchertest = new FormVoucher();
            formVouchertest.showFromAll();
            
            formVouchertest.setVisible(true);

            // Đóng form hiện tại
            dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm voucher thất bại!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

