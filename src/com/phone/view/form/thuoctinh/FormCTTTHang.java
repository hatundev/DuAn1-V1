    public void handleEvent(String name, String tenBang1) {
        txtTitleCTTT.setText(name);
        txtTenCTTT.setText("Tên " + name);
        this.tenBang = tenBang1;
        fillTable();
        getCbo();
    }

    public void setComboBoxValue(int hoatDong) {
        if (hoatDong == 1) {
            jComboBox1.setSelectedItem("Đang hoạt động");
        } else {
            jComboBox1.setSelectedItem("Không hoạt động");
        }
    }

    public void fillForm(ThuocTinhResponse thuocTinhResponse) {
        txtID.setText(String.valueOf(thuocTinhResponse.getId()));
        txtHang.setText(String.valueOf(thuocTinhResponse.getName()));
        setComboBoxValue(thuocTinhResponse.getHoatDong());
    }

    public void getCbo() {
        jComboBox1.removeAllItems();
        jComboBox1.addItem("Đang hoạt động");
        jComboBox1.addItem("Không hoạt động");
    }

    public void fillTable() {
        defaultTableModel = (DefaultTableModel) tbList.getModel();
        defaultTableModel.setColumnIdentifiers(new Object[]{"ID", "Tên " + tenBang, "Hoạt Động"});
        list = impl.findAll(tenBang);
        defaultTableModel.setRowCount(0);
        for (ThuocTinhResponse thuocTinhResponse : list) {
            defaultTableModel.addRow(thuocTinhResponse.toRowTable());
        }
    }

