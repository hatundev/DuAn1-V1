    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtHang.getText().isEmpty()) {
        notiWarring = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Chưa nhập tên " + tenBang);
        notiWarring.showNotification();
        txtHang.requestFocus();
    } else {
        try {
            int id = Integer.parseInt(txtID.getText());
            int hoatDong = jComboBox1.getSelectedItem().equals("Đang hoạt động") ? 1 : 0;
            
            ThuocTinhResponse data = new ThuocTinhResponse();
            data.setName(txtHang.getText());
            
            Boolean ketQua = impl.update1(tenBang, id, data, hoatDong);
            
            if (ketQua) {
                notiSuccess = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_RIGHT, "Cập nhật " + tenBang + " thành công");
                notiSuccess.showNotification();
                fillTable();
                txtHang.setText("");
                txtID.setText("");
                txtHang.requestFocus();
            } else {
                notiWarring = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Cập nhật thất bại");
                notiWarring.showNotification();
            }
        } catch (NumberFormatException e) {
            notiWarring = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "ID không hợp lệ");
            notiWarring.showNotification();
        } catch (Exception e) {
            e.printStackTrace();
            notiWarring = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Lỗi: " + e.getMessage());
            notiWarring.showNotification();
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (txtID.getText().isEmpty()) {
            notiWarring = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Chưa chọn" + tenBang + " để xóa");
            notiWarring.showNotification();
            txtHang.requestFocus();
        } else {
            int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa " + tenBang + " này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                boolean ketQua = impl.delete(tenBang, Integer.valueOf(txtID.getText()));
                if (ketQua) {
                    notiSuccess = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_RIGHT, "Xóa " + tenBang + " thành công");
                    notiSuccess.showNotification();
                    fillTable();
                    txtHang.setText("");
                    txtID.setText("");
                    txtHang.requestFocus();
                } else {
                    notiWarring = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Xóa thất bại");
                    notiWarring.showNotification();
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

